    public void updateTable3() {
        String ids = cust_search.getText();

        try {
            DefaultTableModel model = (DefaultTableModel) tbl.getModel();
            String sgl = "select * from transaction where Acc='" + ids + "' ";
            pst = conn.prepareStatement(sgl);
            rs = pst.executeQuery();

            if (rs.next()) {
                String acc = rs.getString("Acc");
                String name = rs.getString("Name");
                String balance = rs.getString("Balance");
                String t_type = rs.getString("Transaction_type");
                String dat = rs.getString("Date");

                String[] row = {acc, name, balance, t_type, dat};
                model.addRow(row);

            }

        } catch (SQLException ex) {
            Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, null, ex);
        }

    }


