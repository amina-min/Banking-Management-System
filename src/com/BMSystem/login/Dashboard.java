    public void ProfileShowByLogin() {
        try {
            String sql = "select * from account where Acc=? ";
            pst = conn.prepareStatement(sql);
            pst.setString(1, jTextField1.getText());
            rs = pst.executeQuery();

            if (rs.next()) {
                String fild1 = rs.getString("Name");
                jTField1.setText(fild1);

                String fild2 = rs.getString("Nationality");
                jTField2.setText(fild2);

                String fild3 = rs.getString("Gender");
                jTField3.setText(fild3);

                String fild4 = rs.getString("Address");
                jTField4.setText(fild4);

                String fild5 = rs.getString("MobileNo");
                jTField5.setText(fild5);

                String fild6 = rs.getString("Acc");
                jTField6.setText(fild6);

                rs.close();
                pst.close();

            } else {
                JOptionPane.showMessageDialog(null, "Enter the correct Account No.");
            }

        } catch (SQLException ex) {
            Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                rs.close();
                pst.close();
            } catch (Exception e) {

            }
        }

    }
